"""실험 YAML 이름/파일 자동 생성기 (+ 선택적 파이프라인 실행).

목표:
- 팀 규칙에 맞는 파일명을 일관되게 생성
- owner만 입력해도 바로 실행 가능한 실험 YAML 스캐폴드 생성
- 필요 시 같은 스크립트에서 바로 run_pipeline.sh 실행

예시:
    python scripts/new_experiment_yaml.py --owner dm
    python scripts/new_experiment_yaml.py --owner dm --run
    python scripts/new_experiment_yaml.py --owner ab --model y11l --imgsz 896 --data trOnly
"""
from __future__ import annotations

import argparse
import re
import shlex
import shutil
import subprocess
import sys
from datetime import datetime
from pathlib import Path
from typing import Final


REPO_ROOT: Final[Path] = Path(__file__).resolve().parents[1]
EXPERIMENTS_DIR: Final[Path] = REPO_ROOT / "configs" / "experiments"

MODEL_ALIAS: Final[dict[str, str]] = {
    "y11n": "yolo11n",
    "y11s": "yolo11s",
    "y11m": "yolo11m",
    "y11l": "yolo11l",
    "y11x": "yolo11x",
}
ARCH_TO_TOKEN: Final[dict[str, str]] = {v: k for k, v in MODEL_ALIAS.items()}


def _normalize_token(value: str, *, field: str) -> str:
    token = re.sub(r"[^a-zA-Z0-9]+", "_", value.strip().lower()).strip("_")
    if not token:
        raise ValueError(f"{field} 값이 비어 있습니다.")
    return token


def _normalize_model(model_raw: str) -> tuple[str, str]:
    m = _normalize_token(model_raw, field="model")
    if m in MODEL_ALIAS:
        arch = MODEL_ALIAS[m]
        return m, arch

    if m in ARCH_TO_TOKEN:
        token = ARCH_TO_TOKEN[m]
        arch = m
        return token, arch

    # 확장 모델명은 그대로 허용하되 yolo 접두사는 강제
    if not m.startswith("yolo"):
        raise ValueError(
            "지원 모델 형식이 아닙니다. 예: y11s/y11m/y11l 또는 yolo11s/yolo11m/yolo11l"
        )
    token = m.replace("yolo", "y", 1)
    return token, m


def _next_version(stem_prefix: str, directory: Path) -> int:
    pattern = re.compile(rf"^{re.escape(stem_prefix)}_v(\d{{2}})\.yaml$")
    max_ver = 0
    for p in directory.glob("*.yaml"):
        m = pattern.match(p.name)
        if m:
            max_ver = max(max_ver, int(m.group(1)))
    return max_ver + 1


def _build_yaml_text(
    *,
    arch: str,
    data_mode: str,
    optimizer: str,
    imgsz: int,
    epochs: int,
    batch: int,
    seed: int,
) -> str:
    lines: list[str] = [
        "# Auto-generated by scripts/new_experiment_yaml.py",
        '_base_: "../base.yaml"',
        "",
        "model:",
        f'  architecture: "{arch}"',
        f'  pretrained: "{arch}.pt"',
        "",
    ]

    if data_mode == "trExt":
        lines.extend(
            [
                "paths:",
                '  external_images_dir: "data/raw/external/combined/images"',
                "",
                "external_data:",
                "  enabled: true",
                "  sources:",
                '    - name: "combined"',
                '      images_dir: "data/raw/external/combined/images"',
                '      annotations_dir: "data/raw/external/combined/annotations"',
                "      recursive: true",
                "  category_id_mapping:",
                "    enabled: true",
                '    mapping_key: "drug_N"',
                '    fallback_fields: ["drug_N", "dl_mapping_code"]',
                '    on_unmapped: "exclude"',
                "  dedup:",
                "    dedup_against_train: true",
                "",
            ]
        )

    lines.extend(
        [
            "train:",
            f"  epochs: {epochs}",
            f"  imgsz: {imgsz}",
            f"  batch: {batch}",
            f'  optimizer: "{optimizer}"',
            f"  seed: {seed}",
            "  workers: 0",
            "  patience: 16",
            "  verbose: false",
            "  competition_select:",
            "    enabled: true",
            '    candidates: ["best", "last"]',
            '    output_name: "competition_best.pt"',
            "    use_tta: false",
            "",
            "evaluate:",
            "  conf: 0.001",
            "  nms_iou: 0.65",
            "",
            "submission:",
            "  conf: 0.10",
            "  nms_iou: 0.65",
            "  augment: true",
            "  max_det_per_image: 4",
        ]
    )

    return "\n".join(lines) + "\n"


def _build_pipeline_cmd(
    *,
    run_name: str,
    config_rel: str,
    start: int,
    stop: int,
    skip: str,
    device: str | None,
    conf: float | None,
    resume_train: bool,
    auto_resume_train: bool,
    verbose: bool,
) -> list[str]:
    cmd: list[str] = [
        "bash",
        "scripts/run_pipeline.sh",
        "--run-name",
        run_name,
        "--config",
        config_rel,
        "--start",
        str(start),
        "--stop",
        str(stop),
    ]
    if skip.strip():
        cmd += ["--skip", skip.strip()]
    if device is not None and str(device).strip():
        cmd += ["--device", str(device).strip()]
    if conf is not None:
        cmd += ["--conf", str(conf)]
    if resume_train:
        cmd += ["--resume-train"]
    if auto_resume_train:
        cmd += ["--auto-resume-train"]
    if verbose:
        cmd += ["--verbose"]
    return cmd


def main() -> None:
    parser = argparse.ArgumentParser(description="실험 YAML 이름/파일 자동 생성 (+선택 실행)")

    # 생성 파라미터
    parser.add_argument("--owner", required=True, help="실험자 ID (예: dm)")
    parser.add_argument("--model", default="y11m", help="모델 토큰 (예: y11s, y11m, y11l)")
    parser.add_argument("--imgsz", type=int, default=768, help="입력 해상도")
    parser.add_argument("--data", choices=["trOnly", "trExt"], default="trExt", help="데이터 모드")
    parser.add_argument("--optimizer", choices=["adamw", "sgd", "auto"], default="adamw")
    parser.add_argument("--seed", type=int, default=42)
    parser.add_argument("--epochs", type=int, default=48)
    parser.add_argument("--batch", type=int, default=4)
    parser.add_argument("--tag", default="", help="추가 태그 (선택)")

    # 실행 파라미터(선택)
    parser.add_argument("--run", action="store_true", help="YAML 생성 후 run_pipeline.sh를 즉시 실행")
    parser.add_argument("--start", type=int, default=0, help="파이프라인 시작 STAGE (기본: 0)")
    parser.add_argument("--stop", type=int, default=4, help="파이프라인 종료 STAGE (기본: 4)")
    parser.add_argument("--skip", default="", help="건너뛸 STAGE 목록 (예: 1,3)")
    parser.add_argument("--device", default=None, help="디바이스 오버라이드 (예: 0, cpu)")
    parser.add_argument("--conf", type=float, default=None, help="STAGE 4 conf 오버라이드")
    parser.add_argument("--resume-train", action="store_true", help="STAGE 2 --resume 전달")
    parser.add_argument("--auto-resume-train", action="store_true", help="STAGE 2 --auto-resume 전달")
    parser.add_argument("--verbose", action="store_true", help="전체 STAGE verbose")

    # 제어 파라미터
    parser.add_argument("--dry-run", action="store_true", help="파일 생성/실행 없이 이름과 명령만 출력")
    args = parser.parse_args()

    if args.resume_train and args.auto_resume_train:
        raise ValueError("--resume-train 과 --auto-resume-train 은 동시에 사용할 수 없습니다.")
    if args.start < 0 or args.stop < 0 or args.start > args.stop:
        raise ValueError("start/stop 값이 올바르지 않습니다. (0 <= start <= stop)")

    owner = _normalize_token(args.owner, field="owner")
    model_token, arch = _normalize_model(args.model)
    data_mode = args.data
    optimizer = args.optimizer
    seed = int(args.seed)
    imgsz = int(args.imgsz)
    epochs = int(args.epochs)
    batch = int(args.batch)

    if imgsz <= 0:
        raise ValueError("imgsz는 양수여야 합니다.")
    if epochs <= 0:
        raise ValueError("epochs는 양수여야 합니다.")
    if batch <= 0:
        raise ValueError("batch는 양수여야 합니다.")

    tag = _normalize_token(args.tag, field="tag") if args.tag.strip() else ""
    stem = f"{owner}_{model_token}_i{imgsz}_{data_mode}_{optimizer}_s{seed}"
    if tag:
        stem = f"{stem}_{tag}"

    EXPERIMENTS_DIR.mkdir(parents=True, exist_ok=True)
    version = _next_version(stem, EXPERIMENTS_DIR)
    version_token = f"v{version:02d}"
    yaml_name = f"{stem}_{version_token}.yaml"
    yaml_path = EXPERIMENTS_DIR / yaml_name

    now = datetime.now().strftime("%Y%m%d_%H%M%S")
    run_name = f"{now}_{stem}_{version_token}"

    if not args.dry_run:
        yaml_text = _build_yaml_text(
            arch=arch,
            data_mode=data_mode,
            optimizer=optimizer,
            imgsz=imgsz,
            epochs=epochs,
            batch=batch,
            seed=seed,
        )
        yaml_path.write_text(yaml_text, encoding="utf-8")

    try:
        yaml_rel = yaml_path.relative_to(REPO_ROOT).as_posix()
    except Exception:
        yaml_rel = yaml_path.as_posix()

    pipeline_cmd = _build_pipeline_cmd(
        run_name=run_name,
        config_rel=yaml_rel,
        start=int(args.start),
        stop=int(args.stop),
        skip=str(args.skip),
        device=None if args.device is None else str(args.device),
        conf=args.conf,
        resume_train=bool(args.resume_train),
        auto_resume_train=bool(args.auto_resume_train),
        verbose=bool(args.verbose),
    )

    print(f"yaml_path={yaml_rel}")
    print(f"run_name={run_name}")
    print("command=" + " ".join(shlex.quote(x) for x in pipeline_cmd))

    if not args.run or args.dry_run:
        return

    if shutil.which("bash") is None:
        raise RuntimeError("bash 실행기를 찾지 못했습니다. Git Bash 또는 WSL 설치가 필요합니다.")

    print("[INFO] run pipeline:")
    print("  " + " ".join(shlex.quote(x) for x in pipeline_cmd))
    result = subprocess.run(pipeline_cmd, cwd=REPO_ROOT)
    if result.returncode != 0:
        raise SystemExit(result.returncode)


if __name__ == "__main__":
    try:
        main()
    except Exception as e:
        print(f"[ERR] {e}", file=sys.stderr)
        raise SystemExit(2)
